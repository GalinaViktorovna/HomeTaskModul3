package com.HomeTaskModule3.MusicTools;

import java.util.List;

public class MusicToolsShop {
   private List<Guitars>guitarses;
    private List<Piano>pianos;
    private List<Trumplet>trumplets;

    public List<Guitars> getGuitarses() {
        return guitarses;
    }

    public void setGuitarses(List<Guitars> guitarses) {
        this.guitarses = guitarses;
    }

    public List<Piano> getPianos() {
        return pianos;
    }

    public void setPianos(List<Piano> pianos) {
        this.pianos = pianos;
    }

    public List<Trumplet> getTrumplets() {
        return trumplets;
    }

    public void setTrumplets(List<Trumplet> trumplets) {
        this.trumplets = trumplets;
    }
}
